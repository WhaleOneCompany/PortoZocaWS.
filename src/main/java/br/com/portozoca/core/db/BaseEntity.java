package br.com.portozoca.core.db;

import com.fasterxml.jackson.annotation.JsonIgnore;
import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import java.io.Serializable;
import javax.persistence.Column;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.MappedSuperclass;
import javax.persistence.Version;

/**
 * A base entity for all others
 *
 * http://www.devsniper.com/base-entity-class-in-jpa/
 * http://respostas.guj.com.br/892-jpa-com-baseentity--sequence
 * http://docs.jboss.org/hibernate/orm/4.3/manual/en-US/html_single/#types-custom
 */
@MappedSuperclass
@JsonIgnoreProperties(ignoreUnknown = true)
public abstract class BaseEntity implements Serializable {

    private static final long serialVersionUID = 1L;

    @Version
    @JsonIgnore
    protected long version;

    @Id
    @Column
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    protected Long id;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public long getVersion() {
        return version;
    }

    public void setVersion(long version) {
        this.version = version;
    }

}
